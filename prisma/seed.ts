async function main() {
  console.log("Seeding database...");

  // Wipe existing questions to avoid duplicates
  await prisma.sessionQuestion.deleteMany({});
  await prisma.studentResponse.deleteMany({});
  await prisma.question.deleteMany({});
  console.log("Cleared existing questions");

  // Seed achievements
  for (const achievement of achievements) {
    await prisma.achievement.upsert({
      where: { name: achievement.name },
      update: {},
      create: achievement,
    });
  }
  console.log(`Seeded ${achievements.length} achievements`);

  // Seed questions per course
  for (const question of worldHistoryQuestions) {
    await prisma.question.create({ data: question });
  }
  console.log(`Seeded ${worldHistoryQuestions.length} AP World History questions`);

  for (const question of cspQuestions) {
    await prisma.question.create({ data: question });
  }
  console.log(`Seeded ${cspQuestions.length} AP Computer Science Principles questions`);

  for (const question of physics1Questions) {
    await prisma.question.create({ data: question });
  }
  console.log(`Seeded ${physics1Questions.length} AP Physics 1 questions`);

  for (const question of satMathQuestions) {
    await prisma.question.create({ data: question });
  }
  console.log(`Seeded ${satMathQuestions.length} SAT Math questions`);

  for (const question of satRWQuestions) {
    await prisma.question.create({ data: question });
  }
  console.log(`Seeded ${satRWQuestions.length} SAT Reading & Writing questions`);

  for (const question of actMathQuestions) {
    await prisma.question.create({ data: question });
  }
  console.log(`Seeded ${actMathQuestions.length} ACT Math questions`);

  for (const question of actEnglishQuestions) {
    await prisma.question.create({ data: question });
  }
  console.log(`Seeded ${actEnglishQuestions.length} ACT English questions`);

  for (const question of actScienceQuestions) {
    await prisma.question.create({ data: question });
  }
  console.log(`Seeded ${actScienceQuestions.length} ACT Science questions`);

  for (const question of actReadingQuestions) {
    await prisma.question.create({ data: question });
  }
  console.log(`Seeded ${actReadingQuestions.length} ACT Reading questions`);

  const total = worldHistoryQuestions.length + cspQuestions.length + physics1Questions.length +
    satMathQuestions.length + satRWQuestions.length +
    actMathQuestions.length + actEnglishQuestions.length + actScienceQuestions.length + actReadingQuestions.length;
  console.log(`\nTotal questions seeded: ${total}`);
  console.log("Database seeded successfully!");
}

main()
  .catch((e) => { console.error(e); process.exit(1); })
  .finally(async () => { await prisma.$disconnect(); });
