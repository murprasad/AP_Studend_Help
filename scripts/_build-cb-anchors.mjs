/**
 * Extract a compact, EDGE-SAFE subset of real CB/ACT exemplars from the fs-based
 * corpus (data/cb-corpus/*.json) into a bundled TS module (src/lib/cb-corpus-anchors.ts).
 *
 * WHY: cb-corpus.ts uses readFileSync → only works on the Node (cron) path. The
 * LIVE practice generator runs on the Cloudflare edge (no fs), so it never got
 * CB grounding. This bakes a small set of real exemplars into the bundle so the
 * live path can ground on real CB/ACT items too (the #1 fidelity lever).
 *
 * Picks N exemplars per section closest to the median stem length (representative,
 * not outliers), with non-empty options. Run: node scripts/_build-cb-anchors.mjs
 */
import { readFileSync, readdirSync, writeFileSync, existsSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join, resolve } from "node:path";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const corpusDir = join(root, "data", "cb-corpus");
const PER_SECTION = 3;
const STEM_CAP = 850; // keep bundle small; generator prompt slices to 500 anyway

if (!existsSync(corpusDir)) { console.error("no data/cb-corpus dir"); process.exit(1); }

// Load same shape as cb-corpus.ts
const all = [];
for (const file of readdirSync(corpusDir).filter((f) => f.endsWith(".json"))) {
  const test = JSON.parse(readFileSync(join(corpusDir, file), "utf8"));
  for (const m of test.modules ?? []) {
    for (const q of m.questions ?? []) all.push({ ...q, section: m.section });
  }
}
const wc = (s) => (s || "").trim().split(/\s+/).filter(Boolean).length;
// Strip PDF-parse noise: leading question number + dash runs ("21 - - - - - -"),
// stray dash sequences, and collapsed whitespace.
const cleanStem = (s) =>
  String(s || "")
    .replace(/^[~∼˜\s]+/, "")                     // leading tilde-variant parse artifact
    .replace(/^\s*\d+\s*(?:[-–—]\s*){2,}/, "")   // leading "21 - - - -"
    .replace(/^\d{1,3}\s+(?=[A-Z])/, "")          // bare leading question number "15 A proposal"
    .replace(/(?:\s[-–—]){3,}\s*/g, " ")          // runs of " - - - -"
    .replace(/\s{2,}/g, " ")
    .replace(/^[^A-Za-z0-9(]+/, "")               // any leading symbol junk (tilde variants etc.); keep leading digits for math
    .trim();

// Per-section content validity. The ACT corpus parser bled ~14% of ACT Math
// items into ACT_ENGLISH (verified 2026-06-09); and PDF→text mangles math
// notation (superscripts lost → "h15q7"). Exclude wrong-subject / mangled
// exemplars so grounding only ever sees genuinely correct, clean items.
const MATH_CUE = /triangle|isosceles|\bangle\b|equivalent to|\bexpression\b|perimeter|\bvertex\b|hypotenuse|solve for|\bvalue of (?:x|y|z)\b/i;
const MANGLED_MATH = /[a-z]\d+[a-z]\d|\b[a-z]\d{2,}\b/i; // "h15q7", "h5q21"
function sectionValid(q, section) {
  const s = cleanStem(q.stem);
  if (section === "ACT_ENGLISH") return !MATH_CUE.test(s);     // drop bled-in math
  if (section === "MATH") return !MANGLED_MATH.test(s);        // drop mangled-notation math
  return true;
}
const sections = ["READING_WRITING", "MATH", "ACT_ENGLISH", "ACT_READING", "ACT_SCIENCE"];

const out = {};
// Junk ratio: fraction of chars that are PDF-table noise (tildes, dash runs,
// pipes, underscores). High junk = a mangled table/figure that doesn't survive
// PDF→text and makes a poor grounding exemplar.
const junkRatio = (s) => {
  const t = cleanStem(s);
  if (!t) return 1;
  const junk = (t.match(/[~|_]|[-–—]{2,}|\d{4,}/g) || []).join("").length;
  return junk / t.length;
};
for (const section of sections) {
  let pool = all.filter(
    (q) => q.section === section && q.stem && q.options && Object.keys(q.options).length >= 3
  );
  if (pool.length === 0) { out[section] = []; continue; }
  // Per-section content validity (wrong-subject / mangled exclusion).
  const valid = pool.filter((q) => sectionValid(q, section));
  if (valid.length >= PER_SECTION) pool = valid;
  // Prefer prose-clean stems; relax the threshold only if too few remain.
  const clean = pool.filter((q) => junkRatio(q.stem) < 0.03 && wc(q.stem) >= 5);
  if (clean.length >= PER_SECTION) pool = clean;
  const lens = pool.map((q) => wc(q.stem)).sort((a, b) => a - b);
  const median = lens[Math.floor(lens.length / 2)];
  // closest-to-median first → representative exemplars
  const ranked = [...pool].sort((a, b) => Math.abs(wc(a.stem) - median) - Math.abs(wc(b.stem) - median));
  out[section] = ranked.slice(0, PER_SECTION).map((q) => ({
    stem: cleanStem(q.stem).slice(0, STEM_CAP),
    options: q.options,
  }));
}

const header = `// AUTO-GENERATED by scripts/_build-cb-anchors.mjs — do not edit by hand.
// Edge-safe bundled CB/ACT exemplars (real official-test items, extracted from
// data/cb-corpus). The full corpus loader (cb-corpus.ts) is fs-based / Node-only;
// this compact subset lets the LIVE (Cloudflare edge) generator ground on real
// CB/ACT items too — the #1 fidelity lever. Regenerate when the corpus changes.

export type AnchorSection = "READING_WRITING" | "MATH" | "ACT_ENGLISH" | "ACT_READING" | "ACT_SCIENCE";
export interface CbAnchor { stem: string; options: Record<string, string>; }

export const CB_CORPUS_ANCHORS: Record<AnchorSection, CbAnchor[]> = ${JSON.stringify(out, null, 2)};

/** Format an anchor as a few-shot example block (mirrors cb-corpus.formatAnchorForPrompt). */
export function formatAnchor(a: CbAnchor, idx: number): string {
  const opts = Object.entries(a.options).map(([k, v]) => \`  \${k}) \${v}\`).join("\\n");
  return \`EXAMPLE \${idx} (from an official CB/ACT practice test):\\nStem: \${a.stem.slice(0, 500)}\\nOptions:\\n\${opts}\`;
}
`;

const outPath = join(root, "src", "lib", "cb-corpus-anchors.ts");
writeFileSync(outPath, header, "utf8");
const counts = sections.map((s) => `${s}:${out[s].length}`).join(" ");
const kb = (Buffer.byteLength(header) / 1024).toFixed(1);
console.log(`✅ Wrote src/lib/cb-corpus-anchors.ts (${kb} KB) — ${counts}`);
